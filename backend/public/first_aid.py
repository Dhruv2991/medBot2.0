# first_aid.py
# Contains safe, non-diagnostic first-aid advice (PUBLIC USE)

FIRST_AID_GUIDE = {
    "cut": (
        "Clean the wound gently with clean water. "
        "Apply an antiseptic if available and cover it with a clean plaster or bandage."
    ),

    "burn": (
        "Cool the burn under running water for at least 10 minutes. "
        "Do not apply ice, oil, or toothpaste. Cover loosely with a clean cloth."
    ),

    "fever": (
        "Rest well and drink plenty of fluids. "
        "Use a cool compress. If fever persists, consult a doctor."
    ),

    "headache": (
        "Rest in a quiet, dark room and drink water. "
        "Avoid screens and stress."
    ),

    "sprain": (
        "Rest the affected area, apply ice, and avoid putting weight on it."
    ),

    "cut finger": (
        "Wash the finger with clean water, stop any bleeding, "
        "and cover it with a sterile bandage or plaster."
    )
}
